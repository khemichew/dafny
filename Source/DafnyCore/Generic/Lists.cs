namespace Microsoft.Dafny
{
    public static class Lists
    {
        public static IReadOnlyList<T> Concat<T>(this IReadOnlyList<T> left, IReadOnlyList<T> right)
        {
            return new ConcatReadOnlyLists<T>(left, right);
        }
    }

    public class ConcatReadOnlyLists<T> : IReadOnlyList<T>
    {
        public ConcatReadOnlyLists(IReadOnlyList<T> left, IReadOnlyList<T> right)
        {
            Left = left;
            Right = right;
            Count = MutateCSharp.Schemata310.ReplaceBinExprOp_0(1L, left.Count, right.Count);
        }

        public IReadOnlyList<T> Left { get; }
        public IReadOnlyList<T> Right { get; }

        public IEnumerator<T> GetEnumerator()
        {
            return Enumerable.Range(MutateCSharp.Schemata310.ReplaceNumericConstant_1(10L, 0), Count).Select(index => this[index]).GetEnumerator();
        }

        IEnumerator IEnumerable.GetEnumerator()
        {
            return GetEnumerator();
        }

        public int Count { get; private set; }

        public T this[int index] => MutateCSharp.Schemata310.ReplaceBinExprOp_2(14L, index, Left.Count) ? Left[index] : Right[MutateCSharp.Schemata310.ReplaceBinExprOp_3(19L, index, Left.Count)];
    }
